class Command(BaseCommand):
    help = "Создаёт демонстрационные статьи базы знаний"

    def handle(self, *args, **options):
        base_date = timezone.make_aware(datetime(2026, 6, 3, 10, 0))
        for order, (category_name, title, summary, content, minutes) in enumerate(ARTICLES, start=1):
            category, _ = ArticleCategory.objects.get_or_create(
                name=category_name, defaults={"order": order}
            )
            _, created = Article.objects.get_or_create(
                title=title,
                defaults={
                    "category": category,
                    "summary": summary,
                    "content": content,
                    "reading_minutes": minutes,
                    "is_published": True,
                    "published_at": base_date + timedelta(days=order - 1),
                },
            )
            self.stdout.write(("Создана: " if created else "Уже существует: ") + title)
